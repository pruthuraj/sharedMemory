        if (suppressClickKey === key) {
            suppressClickKey = null;
            return;
        }
        const wasExpanded = expandedNodes.has(key);
        toggleNodeExpanded(key, div);
        if (wasExpanded) {
            clearActiveSelection({ resetLayout: true });
            return;
        }
